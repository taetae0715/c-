#ex1.py - 기본자료형 4가지: 정수, 실수, 논리, 문자열

a = 1004            # 10진수 정수 int
b = '조태정'         # 문자열 String
c = True            # 논리값 boolean
g = 3.14            # 실수 float, double
print('a=', a)
print('b='+b)
print('c=', c)
print('g=', g)

d = 0b1101011      # 2진수
e = 0o674          # 8진수
f = 0x3e5          # 16진수
print('d=', d)
print('e=', e)
print('f=', f)

